import csv
with open('texts.csv', 'r') as f:
    reader = csv.reader(f)
    texts = list(reader)

with open('calls.csv', 'r') as f:
    reader = csv.reader(f)
    calls = list(reader)

max = 0
tele = 0
for c in calls:
    dur = int(c[3])
    if dur > max:
        max = dur
        tele = c[1]

print(f'{tele} spend the longest time, {max} seconds, on the phone during September 2016.')

